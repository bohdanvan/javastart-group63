public class Types {

    public static void main(String[] args) {
        // Primitive types

        // 1. Integer types
        byte b = 100; // 1 B
        short s = 10000; // 2 B
        int i = 1000000000; // 4 B
        long l = 1000000000000000000L; // 8 B

        // 2. Floating point
        float f = 10.5f; // 4 B
        double d = 10.5f; // 8 B

        // 3. Character
        char c = 'a'; // 2 B

        // 4. Logical
        boolean bool = true;

        // Object types
        String str = "Java";
    }
}
