/**
 * @author bvanchuhov
 */
public class RangePrinter10To30WithWhile {

    public static void main(String[] args) {
        int n = 10;

        while (n <= 30) {
            System.out.println(n);
            n++;
        }
        System.out.println("Goodbye");
    }
}
